# sentencia de control while

# la setencia de control while 
# se utiliza para realizar algoritmos
# un cierto número de veces (iteraciones)
# teniendo en cuenta que no se conoce
# el número de veces que se realizará
# el algoritmo o los algoritmos

# en este ejemplo, nosotros delimitaremos
# la cantidad de iteraciones con una
# variable declarada como "count"
count = 0
while (count<=9):
    print("The count is: ",count)
    count +=1 # count = count + 1
print("The count finished")


