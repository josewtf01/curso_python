
# operaciones con numeros reales
print("\n números reales \n")

# 5 elevado al cuadrado = 25
print("Potencia:")
print(" 5 ** 2 = ", 5 ** 2, "\n")

# 5 por 3 = 15
print("multiplicación:")
print(" 5 * 3 = ", 5 * 3, "\n")

# 17 entre 3 = 5.666666666...
print("División (clásica):")
print(" 17 / 3 = ", 17 / 3, "\n")

# 17 entre 3 = 5
print("División a suelo:")
print(" 17 // 3 = ", 17 // 3, "\n")

# 17 modulo 3 = 2
print("Residuo (modulo):")
print(" 17 % 3 = ", 17 % 3, "\n")

# 15 + 2 = 17
print("Suma:")
print(" 15 + 2 = ", 15 + 2, "\n")

# 17 - 2 = 15
print("Resta:")
print(" 17 - 2 = ", 17 - 2, "\n")

#operaciones con números complejos

print("\n Números complejos \n")

# (1 + 1j) elevado al  cuadrado =  2j
print("Potencia:")
print(" (1 + 1j) ** 2 = ", (1 +1j) ** 2, "\n")

# (1 + 1j) por 3 =  (3+3j)
print("multiplicación por número real:")
print(" (1 + 1j) * 3 = ", (1 + 1j) * 3, "\n")

# (1 + 2j) por 2j =  (-4+2j)
print("multiplicación por número imaginario:")
print(" (1 + 2j) * 2j = ", (1 + 2j) * 2j, "\n")

# (1 + 1j) por (3 + 2j) =  (1+5j)
print("multiplicación entre numeros complejos:")
print(" (1 + 1j) * (3 + 2j) = ", (1 + 1j) * (3 + 2j), "\n")

# (1 + 1j) / 2 =  (0.5+0.5j)
print("División entre un número real:")
print(" (1 + 1j) / 2 = ", (1 + 1j) / 2, "\n")

# (3 + 2j) / 1j =  (2-3j)
print("División entre un número imaginario:")
print(" (3 + 2j) / 1j = ", (3 + 2j) / 1j, "\n")

# (2 + 3j) entre (1 + 1j) =  (2.5+0.5j)
print("División entre números complejos:")
print(" (2 + 3j) / (1 + 1j) = ", (2 + 3j) / (1 + 1j),"\n")

# (1 + 1j) más (2 + 2j) =  (3+3j)
print("Suma de números complejos:")
print(" (1 + 1j) + (2 + 2j) = ", (1 + 1j) + (2 + 2j),"\n")

# (1 + 1j) - (2 + 2j) =  (-1-1j)
print("Resta de números complejos:")
print(" (1 + 1j) - (2 + 2j) = ", (1 + 1j) - (2 + 2j),"\n")
