# Lógica de operadores

# Tenemos dos valores booleanos
# los cuales son 
print(True,"\n")
print(False, "\n")

# Tenemos los operadores lógicos

# 4 == 2**2 -> True, "==" igual que 
print("4 == 2**2 -> ",4 == 2**2,"\n")

# 4 != 2*3 -> True, "!=" diferente de
print("4 != 2*3 ->",end=" ") # *
print(4 != 2*3, "\n")
''' *
En la función print, podemos 
ingresar diferentes parametros
en este caso le dijimos que 
terminará la linea con un espacio
y no con un salto de línea
'''

# 3 < 5 -> True, "<" menor que
print("3 < 5 ->",end = " ")
print(3 < 5 ,"\n")

# 4 > 6 -> False, ">" mayor que
print("4 > 6 ->",end = " ")
print(4 > 6,"\n")

# 4*2 <= 2**3 -> True
# "<=" menor o igual que
print("4*2 <= 2**3 ->",end = " ")
print(4*2 <= 2**3,"\n")

# 1*2*3 >= 1+2+3 -> True,
# ">=" mayor o igual que
print("1*2*3 >= 1+2+3 ->",end = " ")
print(1*2*3 >= 1+2+3,"\n")

